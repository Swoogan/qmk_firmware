uint8_t mod_state;
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    // Store the current modifier state in the variable for later reference
    mod_state = get_mods() | get_oneshot_mods();
    switch (keycode) {

        case PUNCT1:
            {
                static bool exlmkey_registered;
                if (record->event.pressed) {
                    if (mod_state & MOD_MASK_SHIFT) {
                        register_code(KC_1);
                        exlmkey_registered = true;
                    }
                    else {
                        register_code(KC_COMM);
                    }
                    return false;
                } else { // on release of ,
                    if (exlmkey_registered) {
                        unregister_code(KC_1);
                        exlmkey_registered = false;
                    } else {
                        unregister_code(KC_COMM);
                    }
                    return false;
                }
            }
        case PUNCT2:
            {
                static bool dotkey_registered;
                if (record->event.pressed) {
                    if (mod_state & MOD_MASK_SHIFT) {
                        register_code(KC_SLSH);
                        dotkey_registered = true;
                    }
                    else {
                        register_code(KC_DOT);
                    }
                    return false;
                } else { // on release of ,
                    if (dotkey_registered) {
                        unregister_code(KC_SLSH);
                        dotkey_registered = false;
                    } else {
                        unregister_code(KC_DOT);
                    }
                    return false;
                }
            }

        case PUNCT3:
            {
                static bool semikey_registered;
                if (record->event.pressed) {
                    if (mod_state & MOD_MASK_SHIFT) {
                        register_code(KC_5);
                        semikey_registered = true;
                    }
                    else {
                        register_code(KC_SCLN);
                    }
                    return false;
                } else { // on release of ,
                    if (semikey_registered) {
                        unregister_code(KC_5);
                        semikey_registered = false;
                    } else {
                        unregister_code(KC_SCLN);
                    }
                    return false;
                }
            }
    }

    return true;
}

